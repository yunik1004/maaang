import { describe, it, expect } from 'vitest';
import { run } from '../src/lib/interpreter';

// ─── push ─────────────────────────────────────────────────────────────────────

describe('push', () => {
	it('망 → 0', () => expect(run('망 망?')).toBe('0'));
	it('마앙 → 1', () => expect(run('마앙 망?')).toBe('1'));
	it('마아앙 → 2', () => expect(run('마아앙 망?')).toBe('2'));
	it('마아아앙 → 3', () => expect(run('마아아앙 망?')).toBe('3'));
	it('마아아아앙 → 4', () => expect(run('마아아아앙 망?')).toBe('4'));
	it('마아아아아앙 → 5', () => expect(run('마아아아아앙 망?')).toBe('5'));
	it('마앙. → 2  (1 << 1)', () => expect(run('마앙. 망?')).toBe('2'));
	it('마앙.. → 4  (1 << 2)', () => expect(run('마앙.. 망?')).toBe('4'));
	it('마앙... → 8  (1 << 3)', () => expect(run('마앙... 망?')).toBe('8'));
	it('마아앙.. → 8  (2 << 2)', () => expect(run('마아앙.. 망?')).toBe('8'));
	it('마아아아아앙..... → 160  (5 << 5)', () => expect(run('마아아아아앙..... 망?')).toBe('160'));
});

// ─── 산술 ─────────────────────────────────────────────────────────────────────

describe('산술', () => {
	it('add: 2 + 3 = 5', () => expect(run('마아앙 마아아앙 망! 망?')).toBe('5'));
	it('sub: 5 - 2 = 3', () => expect(run('마아아아아앙 마아앙 마앙! 망?')).toBe('3'));
	it('sub 음수: 1 - 3 = -2', () => expect(run('마앙 마아아앙 마앙! 망?')).toBe('-2'));
	it('mul: 3 * 4 = 12', () => expect(run('마아아앙 마아아아앙 마아앙! 망?')).toBe('12'));
	it('div: 7 // 2 = 3  (trunc)', () =>
		expect(run('마앙... 마앙 마앙! 마아앙 마아아앙! 망?')).toBe('3'));
	it('div 0: n // 0 = 0', () => expect(run('마아앙 망 마아아앙! 망?')).toBe('0'));
	it('mod: 7 % 3 = 1', () => expect(run('마앙... 마앙 마앙! 마아아앙 마아아아앙! 망?')).toBe('1'));
	it('mod: 음수 mod → Python 방식 양수 보장', () => {
		// (-1) % 3 = 2  (interpreter는 ((a%b)+b)%b 방식)
		expect(run('망 마앙 마앙! 마아아앙 마아아아앙! 망?')).toBe('2');
	});
	it('shl: 1 << 3 = 8', () => expect(run('마앙 마아아앙 마아아아아앙! 망?')).toBe('8'));
	it('pow: 2 ^ 10 = 1024', () =>
		expect(run('마아앙 마앙... 마아앙 망! 마아아아아아앙! 망?')).toBe('1024'));
});

// ─── 출력 ─────────────────────────────────────────────────────────────────────

describe('출력', () => {
	it('망? → 숫자 출력', () => expect(run('마아아앙 망?')).toBe('3'));
	it('여러 숫자 출력', () => expect(run('마앙 망? 마아앙 망? 마아아앙 망?')).toBe('123'));
	it('마앙? → 문자 출력 (A = 65)', () => {
		// 65 = 64 + 1 = (마아앙.....) + 마앙 + 망!
		expect(run('마아앙..... 마앙 망! 마앙?')).toBe('A');
	});
	it('마앙? → 문자 출력 (0 = 48)', () => {
		// 48 = 16 * 3 = (마앙.... = 1<<4 = 16) * (마아아앙 = 3)
		expect(run('마앙.... 마아아앙 마아앙! 마앙?')).toBe('0');
	});
});

// ─── 스택 조작 ────────────────────────────────────────────────────────────────

describe('스택 조작', () => {
	it('자허 (pop)', () => expect(run('마앙 마아앙 자허 망?')).toBe('1'));
	it('자허... (dup)', () => expect(run('마아앙 자허... 망? 망?')).toBe('22'));
	it('자허! (swap)', () => expect(run('마앙 마아앙 자허! 망? 망?')).toBe('12'));
	// 자허? (over): [1, 2] → 두 번째를 복사 → [1, 2, 1], 출력: 1 2 1
	it('자허? (over)', () => expect(run('마앙 마아앙 자허? 망? 망? 망?')).toBe('121'));
	it('자허.. (store) + 자허. (load)', () => {
		// store 5 at addr 0, load from addr 0
		expect(run('마아아아아앙 망 자허.. 망 자허. 망?')).toBe('5');
	});
	it('다른 주소에 저장/로드', () => {
		// store 3 at addr 0, store 5 at addr 1
		// load addr 1 → 5
		expect(run('마아아앙 망 자허.. 마아아아아앙 마앙 자허.. 마앙 자허. 망?')).toBe('5');
	});
});

// ─── 제어 흐름 ────────────────────────────────────────────────────────────────

describe('제어 흐름', () => {
	it('망!? (label) 은 런타임 no-op', () => expect(run('마아앙 망!? 망?')).toBe('2'));

	it('마아앙!? (if_false_jump): 0이면 건너뜀', () => {
		// push 0 → if_false_jump → skip print(1) → print(2)
		expect(run('망 마아앙!? 마앙 망? 망!? 마아앙 망?')).toBe('2');
	});

	it('마아앙!? (if_false_jump): 비영이면 건너뛰지 않음', () => {
		expect(run('마앙 마아앙!? 마앙 망? 망!? 마아앙 망?')).toBe('12');
	});

	it('마앙!? (goto): 가장 가까운 이전 레이블로 점프', () => {
		// counter = 3, loop: print counter, counter--, if counter == 0 exit
		// 망!? [start] 자허... 망? 마앙 마앙! 자허... 마아앙!? [exit] 마앙!? 망!? [exit] 자허
		expect(run('마아아앙 망!? 자허... 망? 마앙 마앙! 자허... 마아앙!? 마앙!? 망!? 자허')).toBe(
			'321'
		);
	});

	it('카운트다운 루프 (3→1)', () => {
		// push 3, loop: dup, print, sub 1, dup, if 0 exit, goto
		expect(run('마아아앙 망!? 자허... 망? 마앙 마앙! 자허... 마아앙!? 마앙!? 망!? 자허')).toBe(
			'321'
		);
	});
});

// ─── 주석 ─────────────────────────────────────────────────────────────────────

describe('주석', () => {
	it('# 이후 무시', () => expect(run('마아앙 망? # 이건 주석')).toBe('2'));
	it('줄 전체 주석', () => expect(run('# 전체 주석\n마앙 망?')).toBe('1'));
});
